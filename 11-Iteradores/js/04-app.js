//Otro iterador Do while

//a diferencia del for y del whiel, el do while se ejecuta
//al menos una vez, y luego verifica si la condición se cumple

//Declarar e inicializar nuestra variable iteradora
let i = 1000000;

do{
//Bloque de ejecución
    console.log(`Numero es ${i}`);

i++;//incrementamos el contador
}while (i<10);//Condicion

